<h2>Contemporary Adventures</h2>
<p>
  The here and now can be host to a myriad of different campaigns and adventures:
  thrillers, spy and conspiracy stories, military or political events - or used as a backdrop
  for science fiction, horror, detective and noir. The world we're so used to as players
  is an especially rich setting, precisely because it needs no introduction and can provide
  a familiar background where the adventure element can be introduced easily and to great 
  effect,
</p>
<h2>Make Your Own Batteries</h2>
<p>
  For playing contemporary adventures, UD10 gives you a set of rules, a way to create characters
  and NPCs, and some useful guides for the world we live in today. The rest is up to you!
</p>