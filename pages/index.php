<h2>What is it?</h2>
Udo's D10 roleplaying system is an attempt at making a creative commons-licensed rulebook for 
playing <a href="http://en.wikipedia.org/wiki/Tabletop_role-playing_game">pen&paper roleplaying games</a>.

<h2>What's different about it?</h2>
The primary design goal was simplicity: the rules should provide a simple framework for running
any kind of game. UD10 is set up to get your game going very quickly, and it enables
game masters to tell their story without the rules getting in the way.

<h2>Where can I get it?</h2>
All of the UD10 material is available right here, on the project website. You can use
it free of charge, as you like - but please honor <a href="http://creativecommons.org/licenses/by-nc-sa/3.0/">the license</a>.

