<h2>Ingredients of a Fantasy Adventure</h2>
<p>
  Fantasy campaigns and adventures are taking place in a world that functions based on
  rules that are very different from our own. By convention, these worlds contain magic
  (or an equivalent force) as an integral part of the setting. Often, this means the inhabitants
  of these worlds interact with magical forces in a way that we today would interact with
  fire or computer equipment. What follows from this is a setting that is to varying degrees
  based on some variant of medieval Earth - not only because <i>Lord of the Rings</i> and <i>A Song of
  Ice and Fire</i> have told us so but because the presence of magic seems to obviate 
  the development of mundane technology. 
</p>
<p>
  The result is a familiar world that we associate
  with the term "Fantasy": Europe, middle ages, plus magic. However, this is only a superficial
  interpretation, and the GM is invited to depart a little from that restriction and come
  to their own conclusions about the world they want to portray. What's social conventions govern
  the world? Who has the power? What's the planet's history? What are the environmental factors?
  What has magic done to the world so far? Have there been mass extinctions and apocalyptic events
  in the past?
</p>
<h2>The Classics</h2>
<p>
  <a href="http://en.wikipedia.org/wiki/Monomyth">
    <img src="/img/Heroesjourney.svg.png" alt="Heroesjourney.svg" width="250" align="right" />
  </a>
  In general, fantasy adventures portray the classic and stereotypical Path of the Hero
  with greater focus than, say, contemporary or science fiction settings. Campbell's Monomyth
  interpretation can be used to guide the development of an adventure or a campaign, 
  because it's a proven narrative structure in three acts: departure, initiation, and return
  of the hero. The formalization and serialization of this classic structure, as well as
  the ever-influential <i>Lord of the Rings</i>, has resulted in the archetypical fantasy
  adventuring party of diverse and sometimes unlikely heroes who embark on a journey to save
  the world. Departing from this structure means a fantasy setting that can be used to 
  portray different kinds of stories, such as conspiracies and politics, horror and suspense,
  action and combat, conquest and exploration, or really any kind of tale. 
  As always, UD10 aims to give you, the GM, the
  loose framework for rolling your own world and tries to get out of your way as much as
  possible.
</p>
